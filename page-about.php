					<section class="deeper-look">
						<p><a class="normal" href="http://www.marketwatch.com/story/half-of-the-high-paying-jobs-in-america-now-require-this-skill-2016-06-21">According to Market Watch</a>, “roughly half of the jobs in the top income quartile — defined as those paying $57,000 or more per year — are in occupations that commonly require applicants to have at least some computer coding knowledge or skill, according to an analysis of 26 million U.S. online job postings by job market analytics firm Burning Glass and Oracle Academy.”</p>
						<p><a target="_blank" class="normal" href="http://www.modis.com/it-insights/infographics/top-it-jobs-of-2018/">Where Tech Jobs Are Growing &mdash; Information Graphic</a> </p>
						<p>While the tech sector and job market grows so quickly, students without access to critical STEM education are being left behind in the race. The rapid and immense advancement that has been made towards this tech-centric world has been so exponential that it has created a dangerous and unjust divide. The tech industry has birthed new languages, fostered new ways of thinking, and warranted an entire new set of skills. Without access to the necessary education, students are being denied the opportunity to succeed in this world. With tech companies struggling to fill the constantly growing demand for tech professionals, why are women and people of color still being denied the tools to permeate this sector?</p>
						<p><a target="_blank" class="normal" href="http://www.informationisbeautiful.net/visualizations/diversity-in-tech/">Diversity in Tech &mdash; Information Graphic</a></p>
						<p>As sports professionals, we recognize that the path to becoming a professional athlete is clear - early youth skills training, high school recruitment, college scholarship, then the NFL Draft. Starting from childhood, students can choose - and are often encouraged in many communities - to follow this path to “success”. It’s clear, it’s attractive, and it undoubtedly benefits those that are encouraging the students to follow this path. But the problem is that this path only leads to success for 0.01% of high school athletes. <a href="http://www.ncaa.org/about/resources/research/football">According to the NCAA</a>, only 6.8% of high school football players continue on to compete at a collegiate level, with only 1.5% of that percentage moving on to a career as a professional athlete. But student athletes are told that if you just work hard, stay on the grind, and stay out of trouble, you could be the next Dez Bryant, Russell Wilson, or Lebron James. Although we support aspiring athletes, it is time to create a greater pathway for students to follow towards success, prosperity, and leadership - a pathway that is clear, possible, and inclusive of people from all backgrounds, regardless of race, color, creed, or sexual orientation.</p>
						<p>At the intersection of sports and technology, we are leveraging these combined powers in a movement to create a new path to opportunity for students of all backgrounds and to foster diversity in the tech industry. Mirroring the path to professional athleticism, we replace Coaches with Mentors, physical training with learning, and Agents with Hiring Managers. Through our programs and with the help of our influential partners, we have created this new pathway, starting from childhood, and leading to a career in the tech industry as a leader and an innovator.</p>
